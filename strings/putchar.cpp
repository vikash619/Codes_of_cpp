{   
    int ch;
    
    for(ch = 'A'; ch <= 'Z'; ch++)
    putchar(ch);                    // passing 'ch = A' means  we passing "65" integer 
                                    // and it prints 'A' coz 65 = A
                                    
                                    //this function accepts integer argument and return 
                                    //character representing that integer
    
    
    return 0;
}
